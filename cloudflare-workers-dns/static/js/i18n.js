// 获取嵌套对象的值
function getNestedValue(obj, path) {
    if (!obj || !path) return null;
    const keys = path.split('.');
    let value = obj;
    for (const key of keys) {
        if (value && typeof value === 'object' && key in value) {
            value = value[key];
        } else {
            return null;
        }
    }
    return value;
}

// 初始化 Alpine.js i18n store
document.addEventListener('alpine:init', () => {
    console.log('[i18n] Initializing i18n store...');
    Alpine.store('i18n', {
        locale: localStorage.getItem('locale') || 'zh',

        setLocale(lang) {
            console.log('[i18n] setLocale called:', lang);
            this.locale = lang;
            localStorage.setItem('locale', lang);
            document.documentElement.lang = lang === 'zh' ? 'zh-CN' : 'en';
            console.log('[i18n] locale changed to:', this.locale);
        },

        t(key, params) {
            const lang = this.locale;
            const messages = window.i18nData[lang];
            if (!messages) return key;
            let value = getNestedValue(messages, key);
            if (value === null) return key;
            // 支持变量插值：将 {variable} 替换为 params 中的值
            if (params && typeof params === 'object') {
                Object.keys(params).forEach(k => {
                    value = value.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
                });
            }
            return value;
        },

        get currentLanguageName() {
            return this.locale === 'zh' ? '中文' : 'English';
        },

        get otherLocale() {
            return this.locale === 'zh' ? 'en' : 'zh';
        },

        toggleLocale() {
            console.log('[i18n] toggleLocale called, current:', this.locale);
            const newLocale = this.otherLocale;
            this.locale = newLocale;
            localStorage.setItem('locale', newLocale);
            document.documentElement.lang = newLocale === 'zh' ? 'zh-CN' : 'en';
            console.log('[i18n] locale toggled to:', newLocale);
        }
    });
    console.log('[i18n] i18n store initialized, locale:', Alpine.store('i18n').locale);
});

// 全局翻译函数
window.$t = function(key, params) {
    if (window.Alpine && Alpine.store('i18n')) {
        return Alpine.store('i18n').t(key, params);
    }
    const lang = localStorage.getItem('locale') || 'zh';
    const messages = window.i18nData[lang];
    if (!messages) return key;
    let value = getNestedValue(messages, key);
    if (value === null) return key;
    // 支持变量插值：将 {variable} 替换为 params 中的值
    if (params && typeof params === 'object') {
        Object.keys(params).forEach(k => {
            value = value.replace(new RegExp('\\{' + k + '\\}', 'g'), params[k]);
        });
    }
    return value;
};
